/**
 * ceiling Function
 *
 * @since  17/11/2020
 * @access (public)
 *
 * @type     ceiling Function
 * @description Returns ceiling of a number
 * @param {Number}  valueToBeCalculated  Number that wants to be evaluated
 *
 */
export const ceiling = (valueToBeCalculated) => {
  return Math.ceil(valueToBeCalculated);
};
